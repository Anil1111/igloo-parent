package fr.openwide.core.wicket.more.lesscss;

public class WicketLessCssException extends RuntimeException {

	private static final long serialVersionUID = -1761121529431529383L;

	public WicketLessCssException() {
		super();
	}

	public WicketLessCssException(String message, Throwable cause) {
		super(message, cause);
	}

	public WicketLessCssException(String message) {
		super(message);
	}

	public WicketLessCssException(Throwable cause) {
		super(cause);
	}

}
